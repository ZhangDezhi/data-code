#include<stdio.h>
int main()
{
				int i;
				printf("请输入一个四位整数：");
				scanf("%d",&i);
				int a,b,c,d;
				a=i/1000;//求模，得千位数
				d=i%10;//对10求余，得到个位数
				b=(i-1000*a)/100;//去掉千位数，对100求模，得百位数
				c=(i-1000*a-100*b)/10;//去掉千、百位数，对10 求模，得到十位数
				int s;
				s=1000*d+100*c+10*b+a;		//求的逆序后的新整数
				printf("%d逆序后为：%04d\n",i,s);


				return 0;
}
